      NVL(C.CLI_SECOND_ADDRESS_DESC, ' ') cellPhone,
      C.CLI_EMAIL_ADDRESS PREFEREDEMAILADDRESS,
      C.CLI_ALT_EMAIL_ADDRESS ALTERNATEEMAILADDRESS,
      C.CLI_ST_LIC_NAME NAMEONLICENSE,
      C.CLI_LICENSE_TYPE LICENSETYPE,
      (CASE WHEN C.CLI_ACAD_DUES > SYSDATE THEN 'YES' ELSE 'NO' END) ACADEMYDUESDISCOUNT,
      C.CLI_IS_SALESPERSON ISSALESPERSON,
      D.HIGHEST_DEGREE,
      D.PROFESSIONAL_FOCUS,
      D.EMPLOYER_DESCRIPTION,
      D.TIME_SPENT,
      D.YEARS_OF_EXPERIENCE,
      D.IS_SUPERVISOR,
      C.CLI_TITLE JOB_TITLE,
      C.CLI_AGE_OWNER OWNER,
      C.CLI_PARENT_CO PARENT_CORPORATION,
      C.CLI_NUMBER_EMP TOTAL_EMPLOYED
FROM CLIENTS_AND_FACILITATORS C,
     AGENCY_COMPANIES AC,
     table(cic3.LOAD_DEMOGRAPHICS(cli_rid)) D
WHERE C.CLI_AGENCY_ID = AC.AGE_RID(+)
AND LOWER(CLI_EMAIL_ADDRESS)=LOWER(:participant_email_bv)