      C.CLI_MAIN_STATE BUSINESSSTATE,
      C.CLI_MAIN_ZIP BUSINESSZIP,
      C.CLI_MAIN_COUNTRY BUSINESSCOUNTRY,
      regexp_substr(C.CLI_SECOND_ADDRESS,'[^'||chr(13)||']+',1,1) HOMEADDRESS1,
      regexp_substr(C.CLI_SECOND_ADDRESS,'[^'||chr(13)||']+',1,2) HOMEADDRESS2,
      regexp_substr(C.CLI_SECOND_ADDRESS,'[^'||chr(13)||']+',1,3) HOMEADDRESS3,
      C.CLI_SECOND_CITY HOMECITY,
      C.CLI_SECOND_STATE HOMESTATE,
      C.CLI_SECOND_ZIP HOMEZIP,
      C.CLI_SECOND_COUNTRY HOMECOUNTRY,
