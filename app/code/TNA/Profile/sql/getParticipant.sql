SELECT C.CLI_USERNAME USERNAME,
      C.CLI_PASSWORD PASSWORD,
      C.CLI_GENDER GENDER,
      C.CLI_PREFIX PREFIX,
      C.CLI_FIRST_NAME FIRSTNAME,
      C.CLI_MIDDLE_INITIAL MIDDLENAME,
      C.CLI_LAST_NAME LASTNAME,
      C.CLI_SUFFIX SUFFIX,
      C.CLI_RID PARTICIPANT_ID,
      C.CLI_NICKNAME NAME_PREFERENCE,
      C.CLI_MAIDEN_NAME OTHERLASTNAME,
      C.CLI_BIRTHDAY DOB,
      C.CLI_ST_LIC_NUM STATELICENSENUM,
      C.CLI_LIC_ST STATEOFLICENSE,
      C.CLI_NAT_PRODNUM NPN,
      CIC3.F_TNA_DESIGNATIONS(C.CLI_RID) ALLIANCEDESIGNATIONS,
      CIC3.F_OTHER_DESIGNATIONS(C.CLI_DESIGNATIONS) OTHERDESIGNATIONSHELD,
      AC.AGE_AGENCY_NAME EMPLOYER,
      regexp_substr(C.CLI_MAIN_ADDRESS,'[^'||chr(13)||']+',1,1) BUSINESSADDRESS1,
      regexp_substr(C.CLI_MAIN_ADDRESS,'[^'||chr(13)||']+',1,2) BUSINESSADDRESS2,
      regexp_substr(C.CLI_MAIN_ADDRESS,'[^'||chr(13)||']+',1,3) BUSINESSADDRESS3,
